data = 10
myData = 30


data == myData    # 둘이 같은가? 같으면 참, 그렇지 않다면 거짓 output : False

data != myData    # 둘이 다른가? 다르면 참, 그렇지 않다면 거짓 output : True

data > myData     # data가 myData보다 큰가? output : False

data >= myData    # data가 myData보다 크거나 같은가? output : False

data < myData     # data가 myData보다 작은가? output : True

data <= myData    # data가 myData보다 작거나 같은가? : True